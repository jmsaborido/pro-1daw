public abstract class Insecto extends Invertebrado {
    public Insecto(int peso) {
        super(peso);
    }

    @Override
    public int getPeso() {
        return super.getPeso();
    }

    @Override
    public void setPeso(int peso) {
        super.setPeso(peso);
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
